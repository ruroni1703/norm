void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" ");   // где 0 ставится пробел
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)  // закраска
{
    if (pic[row, col] == 0)  // если не закрашен то красится 1
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);    // проверка как закрашивается
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
